package com.nubteam.inventario.pojo;

public class MovimientosPOJO {
	private String producto;
	private String f_entrada;
	private String can_entrada;
	private String f_salida;
	private String can_salida;
	
	public MovimientosPOJO() {}

	public String getProducto() {
		return producto;
	}

	public void setProducto(String producto) {
		this.producto = producto;
	}

	public String getF_entrada() {
		return f_entrada;
	}

	public void setF_entrada(String f_entrada) {
		this.f_entrada = f_entrada;
	}

	public String getCan_entrada() {
		return can_entrada;
	}

	public void setCan_entrada(String can_entrada) {
		this.can_entrada = can_entrada;
	}

	public String getF_salida() {
		return f_salida;
	}

	public void setF_salida(String f_salida) {
		this.f_salida = f_salida;
	}

	public String getCan_salida() {
		return can_salida;
	}

	public void setCan_salida(String can_salida) {
		this.can_salida = can_salida;
	}
	
	
}
