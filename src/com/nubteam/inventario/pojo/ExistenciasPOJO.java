package com.nubteam.inventario.pojo;

public class ExistenciasPOJO {
	
	private String nombre;
	private String tipo;
	private String can_disp;
	
	public ExistenciasPOJO() {}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public String getCan_disp() {
		return can_disp;
	}

	public void setCan_disp(String can_disp) {
		this.can_disp = can_disp;
	}
	
}
